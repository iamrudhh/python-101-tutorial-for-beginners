# normal number
number_one = 1
print(type(number_one))

# number with dot
number_two = 1.0
print(type(number_two))

# number with j (for special math use)
number_three = 3.14j
print(type(number_three))  

# make any number positive
print(abs(+8))

# round off the number
print(round(1.4))
print(round(1.5))
print(round(1.6))

# change number to binary
print(bin(8))

# change number to hex
print(hex(8))
